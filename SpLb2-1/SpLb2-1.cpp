// Глобальные переменные:
HINSTANCE hInst;                                // текущий экземпляр
WCHAR szTitle[MAX_LOADSTRING];                  // Текст строки заголовка
WCHAR szWindowClass[MAX_LOADSTRING];            // имя класса главного окна

HANDLE ProcHandle[4] = { nullptr, nullptr, nullptr, nullptr }; //Дескрипторы процессов
HANDLE TreadHandle[4] = { nullptr, nullptr, nullptr, nullptr }; //Дескрипторы потоков
DWORD ProcId[4] = { 0, 0, 0, 0 }; //Идентификаторы процессов
